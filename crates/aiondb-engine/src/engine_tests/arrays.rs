#[test]
fn unnest_with_ordinality_supports_correlated_pg_catalog_arrays() {
    let engine = EngineBuilder::for_testing().build().unwrap();
    let (session, _) = engine.startup(startup_params()).expect("startup");

    engine
        .execute_sql(
            &session,
            "CREATE TABLE django_probe_composite ( \
                 a INT NOT NULL, \
                 b INT NOT NULL, \
                 CONSTRAINT django_probe_composite_pkey PRIMARY KEY (a, b) \
             )",
        )
        .expect("create probe table");

    let rows = query_rows(
        &engine,
        &session,
        "SELECT c.conname, \
                (SELECT count(*) \
                   FROM unnest(c.conkey) WITH ORDINALITY AS cols(colid, arridx)) \
         FROM pg_catalog.pg_constraint c \
         JOIN pg_catalog.pg_class cl ON c.conrelid = cl.oid \
         WHERE cl.relname = 'django_probe_composite' \
         ORDER BY 1",
    );
    assert_eq!(rows.len(), 1);
    assert_eq!(
        rows[0].values,
        vec![
            Value::Text("django_probe_composite_pkey".to_owned()),
            Value::BigInt(2),
        ]
    );
}

