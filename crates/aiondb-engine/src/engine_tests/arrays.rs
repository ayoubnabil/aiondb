#[test]
fn composite_unique_prefix_filter_returns_all_matching_rows() {
    let engine = EngineBuilder::for_testing().build().unwrap();
    let (session, _) = engine.startup(startup_params()).expect("startup");

    engine
        .execute_sql(
            &session,
            "CREATE TABLE django_content_type ( \
                 id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, \
                 app_label VARCHAR(100) NOT NULL, \
                 model VARCHAR(100) NOT NULL, \
                 CONSTRAINT django_content_type_app_label_model_key UNIQUE (app_label, model) \
             ); \
             INSERT INTO django_content_type (app_label, model) VALUES \
                 ('xtask_django_app', 'post'), \
                 ('xtask_django_app', 'user'), \
                 ('other_app', 'post')",
        )
        .expect("setup composite unique probe");

    let access_path = access_path_for_query(
        &engine,
        &session,
        "SELECT model \
         FROM django_content_type \
         WHERE app_label = 'xtask_django_app' \
         ORDER BY model",
    );
    let composite_prefix_scan = match &access_path {
        aiondb_plan::ScanAccessPath::IndexEqComposite { .. } => true,
        aiondb_plan::ScanAccessPath::IndexOnlyScan { inner, .. } => matches!(
            inner.as_ref(),
            aiondb_plan::ScanAccessPath::IndexEqComposite { .. }
        ),
        _ => false,
    };
    assert!(
        composite_prefix_scan,
        "unexpected access path for composite-prefix equality: {access_path:?}"
    );

    let rows = query_rows(
        &engine,
        &session,
        "SELECT model \
         FROM django_content_type \
         WHERE app_label = 'xtask_django_app' \
         ORDER BY model",
    );
    assert_eq!(
        rows,
        vec![
            Row::new(vec![Value::Text("post".to_owned())]),
            Row::new(vec![Value::Text("user".to_owned())]),
        ]
    );
}

