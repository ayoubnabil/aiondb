//! Quantifies the foundation's reason to exist: a persistent compact
//! projection runs an algorithm with **zero per-query rebuild**, whereas the
//! legacy path rebuilds the compact topology on every single query.
//!
//! Two scenarios per graph size:
//!   * `rebuild_each_query`  -- `CsrGraph::from_edges` + PageRank every call
//!     (the cost the projection store eliminates).
//!   * `persistent_no_rebuild` -- projection built once, PageRank straight on
//!     its cached compact view.
//!
//! The gap between the two is the saved rebuild cost per query.

use aiondb_graph::algorithms::pagerank::pagerank_default;
use aiondb_graph::algorithms::CsrGraph;
use aiondb_graph_projection::PersistentGraphProjection;
use criterion::{black_box, criterion_group, criterion_main, BenchmarkId, Criterion};

/// Deterministic edge list: `nodes` vertices, ~`avg_degree` out-edges each,
/// generated by a SplitMix64-style LCG so the benchmark is reproducible.
fn gen_edges(nodes: u32, avg_degree: u32) -> Vec<(u32, u32)> {
    let mut state: u64 = 0x9E37_79B9_7F4A_7C15;
    let mut next = || {
        state = state.wrapping_add(0x9E37_79B9_7F4A_7C15);
        let mut z = state;
        z = (z ^ (z >> 30)).wrapping_mul(0xBF58_476D_1CE4_E5B9);
        z = (z ^ (z >> 27)).wrapping_mul(0x94D0_49BB_1331_11EB);
        z ^ (z >> 31)
    };
    let mut edges = Vec::with_capacity((nodes as usize) * (avg_degree as usize));
    for u in 0..nodes {
        for _ in 0..avg_degree {
            let v = (next() % u64::from(nodes)) as u32;
            edges.push((u, v));
        }
    }
    edges
}

fn bench(c: &mut Criterion) {
    let mut group = c.benchmark_group("pagerank_rebuild_vs_persistent");
    for &nodes in &[1_000u32, 5_000, 20_000] {
        let edges = gen_edges(nodes, 8);

        group.bench_with_input(
            BenchmarkId::new("rebuild_each_query", nodes),
            &nodes,
            |b, &n| {
                b.iter(|| {
                    let csr = CsrGraph::from_edges(n, &edges);
                    black_box(pagerank_default(&csr));
                });
            },
        );

        let proj = PersistentGraphProjection::from_edges("bench", 1, nodes, &edges);
        group.bench_with_input(
            BenchmarkId::new("persistent_no_rebuild", nodes),
            &nodes,
            |b, _| {
                b.iter(|| {
                    black_box(pagerank_default(proj.view()));
                });
            },
        );
    }
    group.finish();
}

criterion_group!(benches, bench);
criterion_main!(benches);
